    private void cargarProductos() {
        modeloTabla = (DefaultTableModel) jTable1.getModel();
        modeloTabla.setRowCount(0); // limpia la tabla
        List<Product> lista = productDAO.ObtenerProducto();
        for (Product p : lista) {
            modeloTabla.addRow(new Object[]{
                p.getNombre(),
                p.getCategoria(),
                p.getPrecio(),
                p.getCantidad(),
                p.getDescripcion()
            });
        }
    }

