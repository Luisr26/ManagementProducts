    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            String nombre = jTextField1.getText();
            String categoria = jComboBox1.getSelectedItem().toString();
            double precio = Double.parseDouble(jTextField2.getText());
            int cantidad = Integer.parseInt(jTextField3.getText());
            String descripcion = jTextArea1.getText();

            if (nombre.isEmpty() || categoria.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios");
                return;
            }

            Product p = new Product(nombre, categoria, precio, cantidad, descripcion);

            if (modoEditar) {
                productDAO.actualizarProducto(p, nombreSeleccionado);
                JOptionPane.showMessageDialog(this, "✅ Producto actualizado correctamente");
            } else {
                productDAO.InsertarProducto(p);
                JOptionPane.showMessageDialog(this, "✅ Producto guardado correctamente");
            }

            limpiarCampos();
            cargarProductos();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "⚠️ Precio o cantidad no válidos.");
        }
    }

